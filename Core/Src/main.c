void CustomDelayAndProcessTX(uint32_t delay_ms) {
    uint32_t start_time = HAL_GetTick();
    while ((HAL_GetTick() - start_time) < delay_ms) {
        // Якщо пройшла 1 секунда (1000 мс) - відправляємо дані
        if ((HAL_GetTick() - last_tx_time) >= 1000) {
            last_tx_time = HAL_GetTick();
            // Використовуємо переривання, щоб не блокувати цикл
            HAL_UART_Transmit_IT(&huart2, (uint8_t*)"Test5", 5);
        }
    }
}

// Функції для відтворення крапки та тире
void BlinkDot() {
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13, GPIO_PIN_SET);
    CustomDelayAndProcessTX(DOT_MS);
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13, GPIO_PIN_RESET);
    CustomDelayAndProcessTX(DOT_MS); // Пауза між символами однієї букви
}

void BlinkDash() {
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13, GPIO_PIN_SET);
    CustomDelayAndProcessTX(DOT_MS * 3); // Тире = 3 крапки
    HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13, GPIO_PIN_RESET);
    CustomDelayAndProcessTX(DOT_MS);
}

// Перетворення рядка у Морзе
void PlayMorseString(char* str, uint16_t len) {
    for(int i = 0; i < len; i++) {
        char c = toupper(str[i]);
        const char* sequence = NULL;

        if(c >= 'A' && c <= 'Z') sequence = morse_letters[c - 'A'];
        else if(c >= '0' && c <= '9') sequence = morse_numbers[c - '0'];

        if(sequence) {
            for(int j = 0; sequence[j] != '\0'; j++) {
                if(sequence[j] == '.') BlinkDot();
                else if(sequence[j] == '-') BlinkDash();
            }
            // Пауза між буквами (вже була затримка DOT_MS, додаємо ще 2)
            CustomDelayAndProcessTX(DOT_MS * 2);
        }
    }
    // Пауза між словами (зазвичай 7 крапок)
    CustomDelayAndProcessTX(DOT_MS * 4);
}

